package addrbook;

public class AddrBook {
	private int ad_id;
	private String ad_name;
	private String ad_email;
	private String ad_tel;
	private String ad_birth;
	private String ad_comdept;
	private String ad_memo;

	public int getAd_id() {
		return ad_id;
	}

	public void setAd_id(int ad_id) {
		this.ad_id = ad_id;
	}

	public String getAd_name() {
		return ad_name;
	}

	public void setAd_name(String ad_name) {
		this.ad_name = ad_name;
	}

	public String getAd_email() {
		return ad_email;
	}

	public void setAd_email(String ad_email) {
		this.ad_email = ad_email;
	}

	public String getAd_tel() {
		return ad_tel;
	}

	public void setAd_tel(String ad_tel) {
		this.ad_tel = ad_tel;
	}

	public String getAd_birth() {
		return ad_birth;
	}

	public void setAd_birth(String ad_birth) {
		this.ad_birth = ad_birth;
	}

	public String getAd_comdept() {
		return ad_comdept;
	}

	public void setAd_comdept(String ad_comdept) {
		this.ad_comdept = ad_comdept;
	}

	public String getAd_memo() {
		return ad_memo;
	}

	public void setAd_memo(String ad_memo) {
		this.ad_memo = ad_memo;
	}

}